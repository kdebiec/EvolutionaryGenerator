package agh.cs.main;

public class InitialParameters {
    private int width;
    private int height;
    private int startEnergy;
    private int moveEnergy;
    private int plantEnergy;
    private float jungleRatio;
    private int initialNumberOfAnimals;
    private int animalMinimumEnergyToProcreate;

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public int getStartEnergy() {
        return startEnergy;
    }

    public int getMoveEnergy() {
        return moveEnergy;
    }

    public int getPlantEnergy() {
        return plantEnergy;
    }

    public float getJungleRatio() {
        return jungleRatio;
    }

    public int getInitialNumberOfAnimals() {
        return initialNumberOfAnimals;
    }

    public int getAnimalMinimumEnergyToProcreate() {
        return animalMinimumEnergyToProcreate;
    }
}
